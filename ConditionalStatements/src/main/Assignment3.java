package main;

/*
 * Write a class named Assignment3 which creates an integer variable x. Set x to some value. 
 * Then write a conditional statement that checks if x is greater than or equal 3. If so, 
 * print the message, “x is greater than or equal 3” to the console. 
 */

public class Assignment3 {
	
	public static void main (String[]args) {
		
		int x = 4;
		
		if (x >= 3) {
			
			System.out.println(x + " is greater than or equal to 3.");
		}
	}

}
