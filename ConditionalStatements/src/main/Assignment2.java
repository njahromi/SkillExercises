package main;

/*
 * Write a class named Assignment2 which creates an integer variable x.
 * Set x to some value. Then write a conditional statement that checks 
 * if x is greater than 3. If so, print the message, “x is greater than 3” to the console. 
 */

public class Assignment2 {

	public static void main(String[] args) {
		
		int x = 2;
		
		if (x < 3) {
			
			System.out.println(x + " is less than 3.");
		}
		
		else {
			
			System.out.println(x + " is greater than 3.");
		}

	}

}
